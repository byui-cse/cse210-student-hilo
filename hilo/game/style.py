def print_red(text):
    print('\033[31m' + text)


def print_green(text):
    print('\033[32m' + text)


def print_blue(text):
    print('\033[34m' + text)


def print_yellow(text):
    print('\033[33m' + text)


def print_white(text):
    print('\033[37m' + text)


def print_reset():
    print('\033[0m')
